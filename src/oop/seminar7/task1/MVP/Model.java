package oop.seminar7.task1.MVP;

public class Model {
    protected double x;
    protected double y;
    protected double w;
    protected double z;

    protected void setX(double x) {
        this.x = x;
    }

    protected void setY(double y) {
        this.y = y;
    }

    protected void setW(double w) {
        this.w = w;
    }

    protected void setZ(double z) {
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getW() {
        return w;
    }

    public double getZ() {
        return z;
    }
}
